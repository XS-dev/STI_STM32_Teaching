void USART1PutString(char *str)
{
	unsigned int k=0;
  do 
  {
      HAL_UART_Transmit(&huart7,(uint8_t *)(str + k) ,1,1000);
      k++;
  } while(*(str + k)!='\0');
}

int fputc(int ch, FILE *f)
{
	/* 发送一个字节数据到串口DEBUG_USART */
	HAL_UART_Transmit(&huart5, (uint8_t *)&ch, 1, 1000);	
	
	return (ch);
}

